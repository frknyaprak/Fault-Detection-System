    private void tblList(){
        int i = 0;
        removeTbl();
        for(ViewArizaTable item : dbArizaKaydi.tblList(2L)) {
            satirEkle();
            tblAriza.setValueAt(item.getId(), i, 0);
            tblAriza.setValueAt(item.getGirisTarihi(), i, 1);
            tblAriza.setValueAt(item.getPersonelAdi(), i, 2);
            tblAriza.setValueAt(item.getDurumu(), i, 4);
            i++;
        }

    }
    
    private void removeTbl(){
        DefaultTableModel df = (DefaultTableModel)this.tblAriza.getModel();
        df.getDataVector().removeAllElements();
        this.tblAriza.repaint();
    }
    
    private void satirEkle(){
        DefaultTableModel df = (DefaultTableModel)this.tblAriza.getModel();
        df.setRowCount(df.getRowCount()+1);
        this.tblAriza.repaint();
    }

